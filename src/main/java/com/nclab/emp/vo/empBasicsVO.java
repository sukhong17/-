public class empBasicsVO {
	private String empcd;
	private String empnm;
	private String empzipcd;
	private String value;
	public String getEmpcd() {
		return empcd;
	}
	public void setEmpcd(String empcd) {
		this.empcd = empcd;
	}
	public String getEmpnm() {
		return empnm;
	}
	public void setEmpnm(String empnm) {
		this.empnm = empnm;
	}
	public String getEmpzipcd() {
		return empzipcd;
	}
	public void setEmpzipcd(String empzipcd) {
		this.empzipcd = empzipcd;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	@Override
	public String toString() {
		return "empBasicsVO [empcd=" + empcd + ", empnm=" + empnm + ", empzipcd=" + empzipcd + ", value=" + value + "]";
	}
	
	
	

}
